#include <bits/stdc++.h>

int main()
{
    std::string s;
    std::cin >> s;

    if (s == "SUN") std::cout << 7 << std::endl;
    if (s == "MON") std::cout << 6 << std::endl;
    if (s == "TUE") std::cout << 5 << std::endl;
    if (s == "WED") std::cout << 4 << std::endl;
    if (s == "THU") std::cout << 3 << std::endl;
    if (s == "FRI") std::cout << 2 << std::endl;
    if (s == "SAT") std::cout << 1 << std::endl;

    return 0;
}